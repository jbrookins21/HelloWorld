public class HelloWorld {
    public static void main(String[] args){

        String message = "Hello, World!";
        //variable declared and initialized in one statement

        message = "Let me see that Thong! - Sisqo, Modern day Philosopher";
        //variable value reassigned

        //strings are objects
        String babyBoi;
        //String declared using camelCase so it is readable

        boolean writingCodeIsFun;
        boolean rootCanalsAreFun;
        //initialized boolean device

        //integer primitives
        byte answerByte = 42; //can hold number that can be stored in 8bits
        short answerShort = 42; //support smaller range of values
        long answerLong = 42; //ideal for record ids
        int answerInt = 42; //standard for declaring whole number values

        //floating point primitives
        float floatValue = 2.1644f; //for holding precision values
        double doubleValue = 2.1644; //for more precision

        //character primitive holds individual character
        char myChar = 'a';

        System.out.println(message);
    }
}


